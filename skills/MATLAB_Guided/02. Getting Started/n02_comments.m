% This is the exercise for '2. Write Comments in Lines and Blocks'

%% Keyboard shortcuts
% Ctrl + R makes the line a comment.
% Ctrl + T uncomments the line.

%% Inline comments
disp('Hello World') % Prints hello world

%% Continuous line with ...
disp(['Hello ' ...
    'World'])

%% Comment blocks
%{
Hello World
This is a comment block!
%}